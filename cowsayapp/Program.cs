    static string Prompt(string text)
    {
        Console.Write(text + ": ");
        return Console.ReadLine();
    }

    static void PrintBox(string text)
    {
        int width = text.Length;
        Console.WriteLine("+" + new string('-', width + 2) + "+");
        Console.WriteLine("| " + text + " |");
        Console.WriteLine("+" + new string('-', width + 2) + "+");
    }

